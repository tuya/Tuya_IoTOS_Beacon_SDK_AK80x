void delay_us( uint32_t n )
{
    while ( n-- )
    {
    	asm("nop"); asm("nop"); asm("nop"); asm("nop");
        asm("nop"); asm("nop"); asm("nop"); asm("nop");
    	asm("nop"); asm("nop");

    }
}

void delay_ms( uint32_t n )
{
    uint16_t    i = 2630;

    while ( n-- )
    {
        while( i-- )
		{
			asm("nop");
		}
		
        i = 2630;
    }
}

